package asignment_ab49;

public class String_Length_CHar_Index 
{
	
	public static void main(String[] args) {
		String name = "Keerthi Nalla";
		
		System.out.println(name.length());//Length of the string
		System.out.println(name.charAt(4));//char at the string
		System.out.println(name.indexOf('N'));//index of particular char
		System.out.println(name.toUpperCase());//to uppercase
		System.out.println(name.toLowerCase());//to lowercase
	}

}
